namespace DiceMathsters.Domain.Dice
{
    /// <summary>
    /// Represents the standard polyhedral dice types used in Dice Mathsters,
    /// modelled after the classic D&amp;D dice set.
    /// The enum value equals the number of faces on the die.
    /// </summary>
    public enum DiceType
    {
        D4  = 4,
        D6  = 6,
        D8  = 8,
        D10 = 10,
        D12 = 12,
        D20 = 20
    }
}
